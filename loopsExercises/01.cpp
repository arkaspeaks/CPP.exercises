#include <iostream>
using namespace std;


//  Print Numbers from 1 to N
// Write a program that takes an integer N as input and prints all numbers from 1 to N.


int main(){
    int a, N;
    a = 1;
    cout << "the value of N";
    cin >> N;
    while (a<N){
        cout << a <<endl;
        a++;
    }
return 0;
}
